def _clean_line(text: str, limit: int = 120) -> str:
    value = re.sub(r"\s+", " ", str(text or "")).strip()
    if not value:
        return ""
    return value if len(value) <= limit else value[:limit].rstrip() + "..."


def _background_block(base_template: dict) -> str:
    background = dict((base_template or {}).get("00_BACKGROUND", {}) or {})
    profile = dict(background.get("profile", {}) or {})
    experiences = list(background.get("key_experiences", []) or [])

    lines: list[str] = []
    for key, value in profile.items():
        clean = _clean_line(value, 90)
        if clean:
            lines.append(f"{key}: {clean}")
    for item in experiences[:5]:
        clean = _clean_line(item, 120)
        if clean:
            lines.append(f"经历: {clean}")
    return "\n".join(lines) if lines else "（无）"


def _has_persona_material(
    character_voice_card: str,
    base_template: dict,
    style_examples: list[dict],
    display_keywords: list[str] | None = None,
) -> bool:
    if _clean_line(character_voice_card, 40):
        return True
    if any(str(item).strip() for item in list(display_keywords or [])):
        return True
    if any(
        isinstance(item, dict) and _clean_line(item.get("text", ""), 40)
        for item in list(style_examples or [])
    ):
        return True
    for dim in list((base_template or {}).values()):
        if not isinstance(dim, dict):
            continue
        if any(_clean_line(item, 30) for item in list(dim.get("rules", []) or [])):
            return True
        if any(
            isinstance(item, dict) and _clean_line(item.get("item", ""), 20)
            for item in list(dim.get("items", []) or [])
        ):
            return True
        if any(
            isinstance(item, dict) and _clean_line(item.get("pattern", ""), 20)
            for item in list(dim.get("patterns", []) or [])
        ):
            return True
    return False


def _rules_block(base_template: dict) -> str:
    """
    核心语言规则注入块（A–G）。
    - E/F 维度取前 5 条 items
    - B/G 维度取前 5 条 patterns
    - 其余维度取前 4 条 rules
    """
    lines: list[str] = []

    for dim_id, title in CORE_DIM_TITLES.items():
        payload = dict((base_template or {}).get(dim_id, {}) or {})
        section_lines: list[str] = []

        if dim_id in {"E_LIKES", "F_DISLIKES_AND_TABOOS"}:
            for item in list(payload.get("items", []) or [])[:5]:
                if not isinstance(item, dict):
                    clean = _clean_line(item, 90)
                    if clean:
                        section_lines.append(f"- {clean}")
                    continue
                label    = _clean_line(item.get("item", ""), 30)
                behavior = _clean_line(item.get("behavior", ""), 90)
                level    = _clean_line(item.get("level", ""), 12)
                if label:
                    prefix = f"[{label}/{level}]" if level else f"[{label}]"
                    section_lines.append(f"- {prefix} {behavior}".strip())

        elif dim_id in {"B_CATCHPHRASES", "G_AVOID_PATTERNS"}:
            for item in list(payload.get("patterns", []) or [])[:5]:
                if not isinstance(item, dict):
                    clean = _clean_line(item, 90)
                    if clean:
                        section_lines.append(f"- {clean}")
                    continue
                fragments = [
                    _clean_line(item.get("pattern", ""), 50),
                    _clean_line(item.get("usage", ""), 60),
                    _clean_line(item.get("tone", ""), 40),
                    _clean_line(item.get("alternative", ""), 80),
                ]
                merged = " | ".join(part for part in fragments if part)
                if merged:
                    section_lines.append(f"- {merged}")
        else:
            for item in list(payload.get("rules", []) or [])[:4]:
                clean = _clean_line(item, 100)
                if clean:
                    section_lines.append(f"- {clean}")

        if section_lines:
            lines.append(f"[{title}]")
            lines.extend(section_lines)

    return "\n".join(lines) if lines else "（无）"


def _background_tendency_block(base_template: dict) -> str:
    """
    H–K 人格底层注入块。
    每个维度取前 2 条 rules（原版只取第一条，丢失了大量有效约束）。
    """
    lines: list[str] = []
    for dim_id, title in BACKGROUND_DIM_TITLES.items():
        payload = dict((base_template or {}).get(dim_id, {}) or {})
        rules = list(payload.get("rules", []) or [])
        if not rules:
            continue
        dim_lines: list[str] = []
        for rule in rules[:2]:
            clean = _clean_line(rule, 100)
            if clean:
                dim_lines.append(clean)
        if dim_lines:
            lines.append(f"[{title}]")
            lines.extend(f"- {l}" for l in dim_lines)
    return "\n".join(lines) if lines else "（无）"


def _style_examples_block(style_examples: list[dict], limit: int = 5) -> str:
    """
    风格示例注入块。
    limit 参数允许调用方控制注入数量（默认 5，对齐设计师端输出的 4–6 条）。
    """
    cleaned: list[str] = []
    for item in list(style_examples or [])[:limit]:
        if not isinstance(item, dict):
            continue
        text = _clean_line(item.get("text", ""), 120)
        if not text:
            continue
        scene   = _clean_line(item.get("scene", ""), 30)
        emotion = _clean_line(item.get("emotion", ""), 20)
        prefix  = " / ".join(part for part in [scene, emotion] if part)
        cleaned.append(f"- {prefix}: {text}" if prefix else f"- {text}")
    return "\n".join(cleaned) if cleaned else "（无）"


def _affinity_hint(level: str) -> str:
    return AFFINITY_HINTS.get(level, AFFINITY_HINTS["any"])


